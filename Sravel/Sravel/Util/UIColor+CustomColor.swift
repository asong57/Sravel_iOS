import UIKit

extension UIColor {
    static var darkBlue: UIColor {
        return UIColor(red: 38.0 / 255.0, green: 153.0 / 255.0, blue: 251.0 / 255.0, alpha: 1)
    }

    static var blue: UIColor {
        return UIColor(red: 127.0 / 255.0, green: 196.0 / 255.0, blue: 253.0 / 255.0, alpha: 1)
    }
    
    static var midBlue: UIColor {
        return UIColor(red: 188.0 / 255.0, green: 224.0 / 255.0, blue: 253.0 / 255.0, alpha: 1)
    }
    
    static var lightBlue: UIColor {
        return UIColor(red: 241.0 / 255.0, green: 249.0 / 255.0, blue: 255.0 / 255.0, alpha: 1)
    }
    
    static var white: UIColor {
        return UIColor(red: 255.0 / 255.0, green: 255.0 / 255.0, blue: 255.0 / 255.0, alpha: 1)
    }
}
